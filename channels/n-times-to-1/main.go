package main

import "fmt"

func main() {
	fmt.Println("Hi :3")

	n := 100
	c := make(chan int)
	done := make(chan bool)

	for i := 0; i < n; i++ {
		go func() {
			for i := 0; i < 10; i++ {
				c <- i
			}
			done <- true
		}()
	}

	go func() {
		for i := 0; i < n; i++ {
			<-done
		}
		close(c)
	}()

	for v := range c {
		fmt.Println(v)
	}
}
